package Loop;
//build a program by Do-While Loop.

public class DoWhile {
    public static void main(String[] args) {

        int i=1;
        do{
            System.out.println(i+" Bangladesh");
            i++;
        }while(i<=10);

        int j=1;
        do{
            System.out.println(j);
            j++;
        }while(j<=50);

        int k=1;
        do{
            System.out.println(k);
            k=k+2;
        }while(k<=100);

        int x=10;
        do{
            System.out.println(x);
            x--;
        }while(x>=1);
    }
}
